export function heroNameExtractor(stringArr) {
  const nameArr = [];
  stringArr.forEach((string) => {
    const arr = string.split("/");
    const newStr = arr.slice(-1)[0];
    const nameStr = newStr.split(".")[0];
    nameArr.push(nameStr);
  });
  return nameArr;
}

